#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	int	cont;

	cont = 0;
	while (len > 0)
	{
		((char *)b)[cont] = c;
		cont++;
		len--;
	}
	return ((char *)b);
}
