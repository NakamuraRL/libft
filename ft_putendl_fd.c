#include "libft.h"

void	ft_putendl_fd(char *s, int fd)
{
	int	cont;

	cont = 0;
	if (!s)
		return ;
	while (s[cont] != '\0')
	{
		write(fd, &s[cont], 1);
		cont++;
	}
	write(fd, "\n", 1);
}
