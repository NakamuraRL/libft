#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	if (n < 0)
	{
		if (n == -2147483648)
		{
			ft_putnbr_fd(-21474, fd);
			ft_putnbr_fd(83648, fd);
			return ;
		}
		write (fd, "-", 1);
		n = n * -1;
	}
	if (n >= 0 && n <= 9)
	{
		n = n + '0';
		write (fd, &n, 1);
	}
	else
	{
		ft_putnbr_fd(n / 10, fd);
		n = n % 10 + '0';
		write (fd, &n, 1);
	}
}
